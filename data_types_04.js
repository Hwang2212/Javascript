// Primitives types \\
// DataTypes are what they are

// String
const student = 'Sara';
console.log(student);
console.log(typeof student); //get type of data

// Number
const age = 20;
console.log(age);
console.log(typeof age);

// Boolean
const isMarried =true;
console.log(isMarried);
console.log(typeof isMarried);

// Null
const car = null;
console.log(car);
console.log(typeof car);

// Undefined
let test;
console.log(test);
console.log(typeof test);

// Symbol
const sym = Symbol();
console.log(sym);
console.log(typeof sym);

// Reference types \\
// They are objects

// Arrays
const ids = [1,2,3,4,5,6];
console.log(ids);
console.log(typeof ids);

// object literals
const passenger = {
    firstName:'Sara',
    lastName:'Smith',
    age:32
}
console.log(passenger);
console.log(typeof passenger);

// Built-in object
const today = new Date();
console.log(today);
console.log(typeof today);
